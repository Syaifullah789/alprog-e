# artimatika

x=24
y=76

# operasi perjumlahan
hasil = x + y
print(x, "+", y, "=", hasil)

# operasi pengurangan
hasil = x - y
print(x, "-", y, "=", hasil)

# operasi perkalian
hasil = x * y
print(x, "*", y, "=", hasil)

# operasi pembagian
hasil = x / y
print(x, "/", y, "=", hasil)

# operasi modulus
hasil = x % y
print(x, "%", y, "=", hasil)

# operasi pangkat
hasil = x ** y
print(x, "**", y, "=", hasil)

# operasi floor divisiaon
hasil = x // y
print(x, "//", y, "=", hasil)